

package javatutorial;

public class ForLoopDemo {
public static void main(String args[]){
	for(int i=1;i<=10;i++){
		System.out.println("the value of i is "+i);
		System.out.println("i am getting printed "+i+" many time(s) ");
		System.out.println("");
	}
}
}

